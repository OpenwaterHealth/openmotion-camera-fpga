static HAL_StatusTypeDef ICM20948_ReadID(I2C_HandleTypeDef *hi2c, uint8_t *id)
{
    HAL_StatusTypeDef result;
    uint8_t reg = ICM20948_WHO_AM_I_REG;

    // Send the register address to read from
    result = HAL_I2C_Master_Transmit(hi2c, ICM20948_ADDR << 1, &reg, 1, HAL_MAX_DELAY);
    if (result != HAL_OK)
    {
        return result;  // Return if there's an error transmitting the register address
    }

    // Read the WHO_AM_I register
    result = HAL_I2C_Master_Receive(hi2c, ICM20948_ADDR << 1, id, 1, HAL_MAX_DELAY);
    return result;
}

int MPL3115A2R1_ReadID(I2C_HandleTypeDef *hi2c, uint8_t *id)
{
    HAL_StatusTypeDef ret;

    uint8_t trans_data[1] = {MPL3115A2R1_WHO_AM_I_REG}; //0x0C is the "Who am i" register
    uint8_t rec_data[1] = {0x00};

    ret = HAL_I2C_Mem_Read(&hi2c1, MPL3115A2R1_I2C_ADDRESS << 1, trans_data[0], sizeof(trans_data) / sizeof(trans_data[0]), rec_data,sizeof(rec_data) / sizeof(rec_data[0]) , 1000);
    if (ret != HAL_OK)
	{
		return -1;  // Return error if we can't send the register address
	}
    else
	{
    	*id = rec_data[0];
	}

    return 1;  // Return success if the correct device ID is found
}

